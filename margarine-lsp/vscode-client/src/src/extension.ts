export async function activate(context: ExtensionContext) {

  const traceOutputChannel = window.createOutputChannel("Margarine Language Server trace");
  
  // Try to find the LSP binary in the parent directory's target folder
  // The structure is: untitled compiler/lsp/src and untitled compiler/margarine-lsp/vscode-client
  // So we need to go up 2 levels to get to "untitled compiler", then look in "target"
  let command = process.env.SERVER_PATH || "margarine-lsp";
  
  const extensionPath = context.extensionPath;
  const parentDir = path.join(extensionPath, "..", "..");
  const localBinaryDebug = path.join(parentDir, "target", "debug", "margarine-lsp");
  const localBinaryRelease = path.join(parentDir, "target", "release", "margarine-lsp");
  
  // Try release build first, then debug, then use PATH
  if (process.env.SERVER_PATH) {
    command = process.env.SERVER_PATH;
  } else {
    // Use require to check if files exist since fs is not available directly
    try {
      const fs = require("fs");
      if (fs.existsSync(localBinaryRelease)) {
        command = localBinaryRelease;
      } else if (fs.existsSync(localBinaryDebug)) {
        command = localBinaryDebug;
      }
    } catch (e) {
      // Fall back to PATH
    }
  }
  
  const run: Executable = {
    command,
    options: {
      env: {
        ...process.env,
        // eslint-disable-next-line @typescript-eslint/naming-convention
        RUST_LOG: "debug",
      },
    },
  };
  const serverOptions: ServerOptions = {
    run,
    debug: run,
  };
  // If the extension is launched in debug mode then the debug server options are used
  // Otherwise the run options are used
  // Options to control the language client
  let clientOptions: LanguageClientOptions = {
    // Register the server for Margarine documents
    documentSelector: [{ scheme: "file", language: "margarine" }],
    synchronize: {
      // Notify the server about file changes to '.clientrc files contained in the workspace
      fileEvents: workspace.createFileSystemWatcher("**/.clientrc"),
    },
    traceOutputChannel,
  };

  // Create the language client and start the client.
  client = new LanguageClient("margarine-lsp", "Margarine language server", serverOptions, clientOptions);
  // activateInlayHints(context);
  client.start();
}

export function deactivate(): Thenable<void> | undefined {
  if (!client) {
    return undefined;
  }
  return client.stop();
}

